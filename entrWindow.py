def SanalyticHom_stable(N, B):
    k = np.arange(1, N + 1)

    # P(K=k) where K ~ BinomPMF(N, 1/B)
    probs = binom.pmf(k, N, 1 / B)

    return np.log2(N) - (B / N) * np.sum(probs * k * np.log2(k))

